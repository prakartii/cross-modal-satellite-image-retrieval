"""
backend/data/archive_store.py

PURPOSE:
  The satellite image archive — a collection of 100 reference scenes
  with pre-computed feature vectors and embeddings.

  When a user uploads an image, we compare its embedding against all
  archive embeddings using cosine similarity to find the most similar
  historical scenes.

WHY THIS DESIGN:
  In production (ISRO Bhuvan scale), the archive would be:
    - 2.48 million satellite scenes
    - Stored in a FAISS GPU index
    - Embeddings generated by a fine-tuned ResNet/ViT model
    - Queried with Approximate Nearest Neighbor (ANN) search

  For this system, we have 100 scenes with:
    - Hand-crafted feature profiles based on domain knowledge
    - Deterministic embeddings computed from these profiles
    - Realistic Indian coordinates and satellite metadata
    - Diverse scene types (flood, vegetation, urban, agriculture, coastal)

DETERMINISM GUARANTEE:
  Archive embeddings are computed from feature profiles, not random vectors.
  The SAME feature profile ALWAYS produces the SAME embedding.
  This means:
    1. Archive is reproducible (no seed needed)
    2. A flood image → high water_index → high cosine sim with flood archive entries
    3. Results make physical sense (not randomly similar)

  PROOF:
    Archive flood entry: water_index ≈ 0.78
    Uploaded flood image: water_index ≈ 0.74 (from feature extraction)
    Both embeddings point "toward" the water_index dimension
    Dot product is high → high cosine similarity ✓

THUMBNAIL URLS:
  Archive entries use curated Unsplash photo IDs that visually match the scene type.
  These are static display thumbnails — not actual satellite imagery.
  Production would serve real 512×512 scene crops from Bhuvan storage.

INPUT:  None (called at server startup)
OUTPUT: Initialized archive with 100 entries + pre-computed embedding matrix

COMPLEXITY: O(100 × 32) for embedding computation = trivial
"""

