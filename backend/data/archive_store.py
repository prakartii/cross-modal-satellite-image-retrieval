class ArchiveStore:
    """
    In-memory archive of satellite scene reference embeddings.

    On first access, builds 100 scene embeddings from domain-knowledge feature profiles.
    Each scene gets a deterministic embedding based on its category + small variation.

    WHY DETERMINISTIC VARIATION:
      Two flood scenes shouldn't have IDENTICAL embeddings — they're different floods.
      We add tiny deterministic noise based on the scene ID hash:
        rng = numpy.random.default_rng(seed=hash(scene_id) % 2^32)
        noise = rng.normal(0, 0.025, 32)
      This ensures:
        1. Same scene_id → same embedding (deterministic)
        2. Different flood scenes → slightly different embeddings (distinct)
        3. All flood scenes cluster together (noise < signal for discriminative dims)
    """

    def __init__(self) -> None:
        self._initialized = False
        self._entries: list[dict[str, Any]] = []
        self._embeddings: np.ndarray = np.zeros((0, 32))
        self._generator = EmbeddingGenerator()

    def initialize(self) -> None:
        """Build archive embeddings from feature profiles. Called once at startup."""
        if self._initialized:
            return

        entries = []
        embeddings = []

        for scene in _SCENE_CATALOG:
            scene_id  = scene["id"]
            category  = scene["category"]
            profile   = SCENE_PROFILES[category].copy()

            # Add tiny deterministic per-scene variation
            # seed = Python hash of scene_id, clamped to valid numpy seed range
            seed = abs(hash(scene_id)) % (2**31)
            rng = np.random.default_rng(seed=seed)
            noise = rng.normal(0, 0.025, 32).astype(np.float32)
            fvec = np.clip(profile + noise, 0.0, 1.0)

            # Compute embedding using the same generator as the pipeline
            embedding, _ = self._generator.generate(fvec.tolist())
            emb_arr = np.array(embedding, dtype=np.float32)

            # Build the archive entry
            thumb_list = _THUMBS.get(category, [""])
            thumb_idx  = abs(hash(scene_id)) % len(thumb_list)

            entry = {
                "id":           scene_id,
                "satellite":    scene["satellite"],
                "sensor_type":  scene["sensor_type"],
                "location":     scene["location"],
                "lat":          scene["lat"],
                "lng":          scene["lng"],
                "date":         scene["date"],
                "event_type":   scene["event_type"],
                "category":     category,
                "resolution_m": scene["resolution_m"],
                "thumbnail":    thumb_list[thumb_idx],
                "cloud_cover":  float(rng.integers(0, 15)),
                "feature_vector": fvec.tolist(),
                "embedding":    embedding,
            }
            entries.append(entry)
            embeddings.append(emb_arr)

        self._entries    = entries
        self._embeddings = np.array(embeddings, dtype=np.float32)
        self._initialized = True

    @property
    def entries(self) -> list[dict[str, Any]]:
        if not self._initialized:
            self.initialize()
        return self._entries

    @property
    def embeddings(self) -> np.ndarray:
        """Returns (100, 32) matrix of archive unit embeddings."""
        if not self._initialized:
            self.initialize()
        return self._embeddings

    @property
    def size(self) -> int:
        return len(self._entries)


# Module-level singleton — initialized once at server startup
archive_store = ArchiveStore()
